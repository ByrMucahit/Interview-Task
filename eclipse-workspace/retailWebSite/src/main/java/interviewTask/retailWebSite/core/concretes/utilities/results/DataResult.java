package interviewTask.retailWebSite.core.concretes.utilities.results;

import org.json.JSONObject;

public class DataResult<T> extends Result {

private T data;
private int userId;
	
	public DataResult(JSONObject jsonInput, int userId ,boolean success, String message) {
		super(success, message);
		this.data = (T) jsonInput;
		this.userId = userId;
	}
	
	public DataResult(T data, boolean success) {
		super(success);
		this.data = data;
	}
	
	public T getData() {
		return this.data;
	}
	
	public int getUserId() {
		return this.userId;
	}
}

/*
 * Description:
 * --------------
 * This class is generated by Result class. This class gets data and user id.
 *  
 * Parameters:
 * --------------
 * jsonInput: All data
 * userId: user id
 * success: It's state controller
 * message: Detail of processing
 * 
 * Return:
 * data, user id is returned
 *  */
 